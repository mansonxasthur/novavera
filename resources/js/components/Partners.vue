<script>
    import SnackbarComponent from '../mixins/SnackbarComponent';

    export default {
        name: "PropertyRequest",
        mixins: [SnackbarComponent],
        data() {
            return {
                height: 500,
                loading: false,
                partner: {
                    name: null,
                    email: null,
                    phone: null,
                    companyName: null,
                    jobTitle: null,
                    message: null
                },
                nameRules: [
                    v => !!v || 'Name is required',
                    v => (v && v.length <= 30) || 'Name must be less than 30 characters'
                ],
                companyRules: [
                    v => !!v || 'Company Name is required',
                    v => (v && v.length <= 50) || 'Name must be less than 50 characters'
                ],
                jobRules: [
                    v => !!v || 'Name is required',
                    v => (v && v.length <= 50) || 'Name must be less than 50 characters'
                ],
                emailRules: [
                    v => !!v || 'E-mail is required',
                    v => /.+@.+/.test(v) || 'E-mail must be valid'
                ],
                phoneRules: [
                    v => !!v || 'Phone is required',
                    v => (v && v.length <= 15) || 'Phone must be less than 15 characters'
                ],
                messageRules: [
                    v => !!v || 'Message is required',
                    v => (v && v.length <= 150) || 'Message must be less than 150 characters',
                ],
            }
        },
        mounted() {
            this.height = document.documentElement.clientHeight;
        },
        methods: {
            send() {
                if (this.$refs.form.validate()) {
                    let vm = this;
                    vm.loading = true;

                    axios.post('/partners', vm.partner)
                        .then(res => {
                            vm.reset();
                            vm.resetValidation();
                            vm.activateSnackbar('success', res.data.message);
                        })
                        .catch(err => {
                            vm.loading = false;
                            if ('response' in err) {
                                vm.activateSnackbar('error', err.response.data.message);
                            }
                        })
                }
            },
            reset() {
                this.$refs.form.reset();
                this.loading = false;
            },
            resetValidation() {
                this.$refs.form.resetValidation()
            }
        }
    }

</script>

